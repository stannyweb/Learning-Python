sliced_String = "Stan is learning Python"
print(sliced_String[0:5])  # Stan

# Without Start
print(sliced_String[:8])  # stan is

# Without Stop
print(sliced_String[5:])

print(sliced_String)

# With a step parameter
print(sliced_String[0:7:3])  # Sns

# Reversed String trick
print(sliced_String[::-1])
